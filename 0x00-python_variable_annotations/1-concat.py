#!/usr/bin/env python3
"""A type-annotated function to concatenate strings"""


def concat(str1: str, str2: str) -> str:
    """Concatinates two strings

    Args:
        str1 (str): The first string parameter
        str2 (str): The second string parameter

    Returns:
        str: a concatinated string

    """
    return (str1 + str2)
